# Atribuição
a = 3

a = a + 7
print(a)

# Acrescentando valor - Atribuição Aditiva
a += 5
print(a)

# Atribuição Diminutiva
a -= 3
print(a)

# Atribuição Multiplicativa
a *= 2
print(a)

# Atribuição Divisiva
a /= 4
print(a)

# Atribuição Modular
a %= 4
print(a)

# Atruibuição Exponencial
a **= 8
print(a)

# Divião por Inteiro
a //= 255
print(a)
